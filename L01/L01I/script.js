/*i) Ler dois inteiros (variáveis A e B) e imprimir o resultado do quadrado da diferença do primeiro valor pelo segundo.*/

const A = parseInt(prompt("Insira um valor inteiro: "))
const B = parseInt(prompt("Insira outro valor inteiro: "))
const resultado = (A - B)**2

alert("O quadrado da diferença do primeiro valor pelo segundo é " + resultado)
